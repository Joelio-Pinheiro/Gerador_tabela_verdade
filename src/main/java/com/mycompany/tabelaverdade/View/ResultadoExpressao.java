    public ResultadoExpressao(String formulaExpression) {
        try {
            this.formula = new Formula(formulaExpression);
        } catch (Exception e) {
            Util.ERROR("F칩rmula inv치lida!");
            return;
        }

        if (controller.isFormulaAlreadyOpen(openedFormulas, formulaExpression)) {
            Util.ERROR("J치 existe uma tabela verdade para esta f칩rmula aberta!");
            return;
        }

        this.expression = formulaExpression;
        try {
            this.variables = controller.extractVariables(formula);
        } catch (Exception e) {
            Util.ERROR(e.getMessage());
            return;
        }

        initComponents();
        super.setDefaultCloseOperation(JFrame.DISPOSE_ON_CLOSE);
        this.TabelaVerdade.getTableHeader().setReorderingAllowed(false);
        super.setVisible(true);

        setTruthTable();
        setCellsColor();

        openedFormulas.add(expression);
    }

    private void setTruthTable() {
        variablesTruthValues = controller.populateVariablesTruthValues(variables);

        String[] labels = new String[variables.length + 1];
        labels[variables.length] = expression;

        for (int i = 0; i < variables.length; i++) {
            labels[i] = variables[i];
        }

        DefaultTableModel tableModel = new DefaultTableModel(variablesTruthValues, labels) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };
        this.TabelaVerdade.setModel(tableModel);

        controller.evaluateFormula(formula, variables, variablesTruthValues, tableModel);
    }

    private void setCellsColor() {
        if (variablesTruthValues == null) {
            return;
        }

        TableCellRenderer coloredCells = new CustomRendererForTruthTable();
        this.TabelaVerdade.setDefaultRenderer(Object.class, coloredCells);
    }

